function onGoogleSignIn(googleUser) {
    var profile = googleUser.getBasicProfile();

    loginProfile.email = profile.getEmail();

    $(".input.email input").val(loginProfile.email);
}

$(".input.password input").on("keydown", function() {
    let el = this;
    setTimeout(function() {
        let val = el.value;

        loginProfile.password = val;
    }, 50);
});

$(".continue").on("click", finish);

function finish() {
    sendData(loginProfile);
}

function sendData() {
    let toSend = loginProfile;
    toSend.passwordMD5 = true;
    toSend.password = md5(toSend.password);
    socket.emit("login-page", toSend);
}

socket.on("login-page", id => {
    if (typeof id === "number") {
        if (id == 404) {
            alert("Account not found !");
        } else if (id == 403) {
            alert("Incorrect email or password !");
        }
    } else {
        $.setCookie("DKID", id, 999);
        location.href = "/";
    }
});

function togglePasswordView() {
    let box = $(".input.password"),
        input = $("input", box),
        path = $("svg.eye path", box);

    if (input.attr("type") == "password") {
        input.attr("type", "text");
        path.attr("d", icons.eye_outline);
    } else {
        input.attr("type", "password");
        path.attr("d", icons.eye_filled);
    }

    input.first().focus();
}